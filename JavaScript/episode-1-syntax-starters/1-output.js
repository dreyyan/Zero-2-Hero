/* Printing / Output */
// [ Syntax ]
console.log("Hello World!");    // with newline
process.stdout.write("Hello");  // without newline

// [ Variants ]
console.error("ERROR: Invalid Input");                  // serious issues
console.warn("WARNING: Overwriting file!");             // not fatal
console.info("INFO: The server started successfully");  // informational messages
console.debug("DEBUG: Variable x =", 123);              // debugging